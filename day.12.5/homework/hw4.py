#HTML-ში alt ატრიბუტი გამოიყენება <img> ტეგისათვის და აღწერს სურათის ჩანაცვლების ტექსტს. ეს ტექსტი იყენება რამდენიმე სხვადასხვა სიტუაციაში, როგორიცაა:

#თუ სურათი ვერ დაიტვირთება: როდესაც ბრაუზერი ვერ შეძლებს სურათის გამოჩენას (მაგალითად, სურათის ფაილი წაშლილია ან არ არსებობს მითითებული URL), მაშინ alt ატრიბუტი მიეწერება და ეს ტექსტი დაიხატება მომხმარებლის ეკრანზე.

#მაგალითად:


#კოდის კოპირება
#<img src="image.jpg" alt="დავწეროთ აღწერა სურათისთვის">
#ეკრანის კითხვა: alt ატრიბუტი მნიშვნელოვან როლს ასრულებს ადამიანებისათვის, ვინც ეკრანის წამკითხველებს (screen readers) იყენებს. ეს ტექნოლოგია ხმარობს alt ტექსტს, რათა მკითხველს აუხსნას სურათის შინაარსი, რაც აუცილებელია ვიზუალური შეზღუდვების მქონე პირებისთვის.

#SEO (Search Engine Optimization): სურათის სწორი აღწერა alt ატრიბუტით ეხმარება საძიებო სისტემებს უკეთ გაერკვნენ სურათის შინაარსში, რაც შეიძლება სასარგებლო იყოს ვებ-გვერდის რანჟირების გასაუმჯობესებლად.

#მაგალითი:
#html

#<img src="cat.jpg" alt="პიტი კატა თავისი საყვარელი სათამაშოთი">
#ამ შემთხვევაში, თუ სურათი ვერ დაიტვირთება, ან თუ მომხმარებელი გამოიყენებს ეკრანის წამკითხველს, მაშინ ეკრანზე გამოჩნდება ტექსტი: "პიტი კატა თავისი საყვარელი სათამაშოთი".

#ზოგადი რეკომენდაციები alt-ის გამოყენებისთვის:

#alt უნდა იყოს კმაყოფილიც და მარტივად გასაგები.
#თუ სურათი არის მხოლოდ დეკორატიული და არ შეიცავს მნიშვნელოვანი ინფორმაცია (მაგალითად, ერთი ფონური სურათი), მაშინ alt შეიძლება იყოს ცარიელი (alt=""), რათა ეკრანის წამკითხველებმა და საძიებო სისტემებმა გაიგონ, რომ ეს სურათი არ არის მნიშვნელოვანი შინაარსისათვის.

